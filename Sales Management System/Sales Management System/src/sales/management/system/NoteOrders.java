    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (jTextField4.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter the correct username.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (String.valueOf(jPasswordField1.getPassword()).isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter the correct old password.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (String.valueOf(jPasswordField2.getPassword()).isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter the correct new password.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (String.valueOf(jPasswordField3.getPassword()).isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter the correct confirm password.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                if (!String.valueOf(jPasswordField2.getPassword()).equals(String.valueOf(jPasswordField1.getPassword()))) {
                    if (db.checkCorrectPerson(jTextField4.getText(), String.valueOf(jPasswordField1.getPassword()), con)) {
                        if (String.valueOf(jPasswordField2.getPassword()).equals(String.valueOf(jPasswordField3.getPassword()))) {
                            if (String.valueOf(jPasswordField2.getPassword()).length() >= 3) {
                                E2.changePassword1(jPasswordField2);
                                JOptionPane.showMessageDialog(null, "Password has been changed.", "Success", JOptionPane.INFORMATION_MESSAGE);
                                jTextField4.setText("");
                                jPasswordField1.setText("");
                                jPasswordField2.setText("");
                                jPasswordField3.setText("");
                            } else {
                                JOptionPane.showMessageDialog(null, "Password length should be greater than 3.", "Error", JOptionPane.ERROR_MESSAGE);
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "New and Confirm Password did not match.", "Error", JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Username and password did not match.", "Error", JOptionPane.ERROR_MESSAGE);                        
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "New and Old Password should not be same.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }            
        } catch (HeadlessException headlessException) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

