    private void button9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button9ActionPerformed
        try {
            if (jComboBox9.getSelectedIndex() != 0) {
                if ("Edit Person".equals(button9.getLabel())) {
                    button9.setLabel("Update Person");
                    jComboBox9.setEnabled(false);
                    jTextField2.setEnabled(true);
                    jTextField1.setEnabled(true);
                    jTextField3.setEnabled(true);
                    jComboBox11.setEnabled(true);
                    if (((String) jComboBox9.getSelectedItem()).substring(0, 1).equals("S")) {                        
                        jTextField7.setEnabled(true);
                        jSpinner1.setEnabled(true);
                    } else if (((String) jComboBox9.getSelectedItem()).substring(0, 1).equals("M") || ((String) jComboBox9.getSelectedItem()).substring(0, 1).equals("E")) {
                        jTextField8.setEnabled(true);
                    }
                } else {
                    boolean clear = false;
                    if (!jTextField1.getText().isEmpty() && !jTextField2.getText().isEmpty() && jComboBox9.getSelectedIndex() != 0) {
                        if (((String) jComboBox9.getSelectedItem()).substring(0, 1).equals("S") && (int) jSpinner1.getValue() >= 0) {                            
                            clear = true;
                        } else if ((((String) jComboBox9.getSelectedItem()).substring(0, 1).equals("M") || ((String) jComboBox9.getSelectedItem()).substring(0, 1).equals("E")) && !jTextField8.getText().isEmpty()) {
                            clear = true;
                        } else if (((String) jComboBox9.getSelectedItem()).substring(0, 1).equals("C")) {
                            clear = true;
                        }
                    }
                    if (clear) {
                        button9.setLabel("Edit Person");
                        button10.setEnabled(true);
                        jComboBox9.setEnabled(true);
                        jTextField2.setEnabled(false);
                        jTextField1.setEnabled(false);
                        jTextField3.setEnabled(false);
                        jComboBox11.setEnabled(false);
                        jTextField7.setEnabled(false);
                        jSpinner1.setEnabled(false);
                        jTextField8.setEnabled(false);
                        db.EditPerson((String) jComboBox9.getSelectedItem(), jTextField2.getText(), jTextField3.getText(), jTextField1.getText(), (String) jComboBox11.getSelectedItem(), jTextField8.getText(), Integer.toString((int) jSpinner1.getValue()), con);                        
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Select the Person to be Editted.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (HeadlessException headlessException) {
        }
    }//GEN-LAST:event_button9ActionPerformed

